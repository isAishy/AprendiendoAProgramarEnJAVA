package Clase1Mundo;

public class TiposDeDatos {
	public static void main(String[] args) {

		/**
		 *  para guardar numeros
		 */
		int edad = 22;

		/**
		 *  para guardar texto
		 */
		String nombre = "Jose Luis";

		/**
		 *  para guardar cracteres
		 */
		char letra = 'a';

		/**
		 *  para guardar decimales
		 */
		double dinero = 10.25;

		System.out.println("Entero: " + edad);
		System.out.println("String: " + nombre);
		System.out.println("Caracter: " + letra);
		System.out.println("Decimal: " + dinero);
		// System.out.println("Hola, soy " + nombre + ", tengo " + edad + " años de edad.");
	}
}
